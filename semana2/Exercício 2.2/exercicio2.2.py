# A mosa de 2 e 2 é menor que 4.
print((2 + 2) < 4)

# O valor de 7 // 3 é igual a 1 + 1
print(7 // 3 == 1 + 1)

# A soma de 3 ao quadrado e 4 ao quadrado é igual a 25.
print(3 ** 2 + 4 ** 2 == 25)

# A soma de 2, 4 e 6 é maior que 12.
print((2 + 4 + 6) > 12)

# 1387 é divisível por 19 (O resto deve ser 0)
print(1387 % 19 == 0)

# 31 é par (Se o resto da divisão por 2 for 0, é par)
print(31 % 2 == 0)

# O preço mais baixo entre 34.99, 29.95 e 31.50 é menor que 30.00
print(min(34.99, 29.95, 31.50) < 30.00)
